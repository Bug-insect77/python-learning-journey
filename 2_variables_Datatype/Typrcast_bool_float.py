# Float to String
my_float = 5.75
my_str = str(my_float)
print(my_float, my_str, type(my_str))  # 5.75 '5.75' <class 'str'>

# Float to Integer
my_int = int(my_float)
print(my_float, my_int, type(my_int))  # 5.75 5 <class 'int'>
# In Python
# int() truncates the decimal part.